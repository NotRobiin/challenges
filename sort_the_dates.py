def sort_dates(l: list, t: str) -> list:
    return sorted(
        l,
        key=lambda x: (x[6:10], x[3:5], x[:2], x[11:13], x[14:16]),
        reverse=t == "DSC",
    )


dates1 = [
    "18-10-2016_12:09",
    "01-12-2017_20:32",
    "18-10-2016_12:04",
    "19-10-2017_16:20",
    "18-10-2017_16:19",
    "18-10-2016_16:19",
]

print(
    sort_dates(dates1, "ASC") == 
    [
        "18-10-2016_12:04",
        "18-10-2016_12:09",
        "18-10-2016_16:19",
        "18-10-2017_16:19",
        "19-10-2017_16:20",
        "01-12-2017_20:32",
    ],
)

